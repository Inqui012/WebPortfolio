/* 
    자료의 형태
    1. 숫자형 (Number)
        정수형 10진수, 16진수
        실수형 소수점이 있는 수
    2. 문자형 (String)
        일상생활 사용 문자
    3. 논리형 (Boolean)
        true, false 의 값만 가지는 것들
    4. Undefined
        정해지지 않은 값
        값이 들어가 있지 않은경우
    5. Null
        아무것도 참조하지 않은 자료형태
        변수를 초기화한 상태
*/

